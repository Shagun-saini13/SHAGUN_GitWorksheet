// /*Write a function that takes two numbers as input and determines which one is
// greater.*/

//Arrow Function
let greater = (a,b) => {
    if(a>b){
        console.log(`${a} is greater`);
    }
    else{
        console.log(`${b} is greater`);
    }
}
greater(9,3);
